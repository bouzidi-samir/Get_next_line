#include "libft.h"

int	ft_atoi(const char *str)
{
	int	n;
	int	nega;

	n = 0;
	nega = 1;
	while ((*str >= 9 && *str <= 13) || *str == ' ')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			nega = nega * -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		n = (n * 10) + (*str - 48);
		str++;
	}
	return (n * nega);
}
